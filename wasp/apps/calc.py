    def calculate(self,s):
        equation = s
        
        # Normal calculator stuff    
        for i in range(len(s)):
            if (s[i] =="^"):
                equation = s[:i] + "**"+s[i+1:]
            elif (s[i] == ":"):
                equation = s[:i] + "/"+s[i+1:]
        
        try:
            result = eval(equation)
        except: # Error
            result = ""
        
        return str(result)
